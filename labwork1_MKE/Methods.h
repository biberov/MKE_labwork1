///Здесь содержатся функции для вычисления определенных интегралов различными
///численными методами : составные формулы трапеций, Симпсона и квадратуры Гаусса.

/// <summary>
/// Составная формула трапеций для численного интегрирования
/// </summary>
/// <param name="f">Подынтегральная функция</param>
/// <param name="a">Нижний предел интегрирования</param>
/// <param name="b">Верхний предел интегрирования</param>
/// <param name="n">Количество подотрезков разбиения</param>
/// <returns>Приближенное значение определенного интеграла</returns>
/// <remarks>Метод имеет второй порядок точности O(h^2), где h = (b-a)/n</remarks>
double composite_trapezoid(double (*f)(double), double a, double b, int n) {
    double h = (b - a) / n;
    double sum = 0.5 * (f(a) + f(b));

    for (int i = 1; i < n; ++i) {
        sum += f(a + i * h);
    }

    return sum * h;
}

/// <summary>
/// Составная формула Симпсона (метод парабол) для численного интегрирования
/// </summary>
/// <param name="f">Подынтегральная функция</param>
/// <param name="a">Нижний предел интегрирования</param>
/// <param name="b">Верхний предел интегрирования</param>
/// <param name="n">Количество подотрезков разбиения (будет округлено до четного)</param>
/// <returns>Приближенное значение определенного интеграла</returns>
/// <remarks>
/// Метод имеет четвертый порядок точности O(h^4).
/// Если n нечетное, оно автоматически увеличивается на 1 для обеспечения четности.
/// </remarks>
double composite_simpson(double (*f)(double), double a, double b, int n) {
    if (n % 2 != 0) n++;

    double h = (b - a) / n;
    double sum = f(a) + f(b);

    for (int i = 1; i < n; i += 2) {
        sum += 4.0 * f(a + i * h);
    }

    for (int i = 2; i < n; i += 2) {
        sum += 2.0 * f(a + i * h);
    }

    return sum * h / 3.0;
}

/// <summary>
/// Получение узлов и весов для квадратуры Гаусса-Лежандра с 3 узлами
/// </summary>
/// <param name="t">Выходной массив для узлов (размер 3)</param>
/// <param name="c">Выходной массив для весов (размер 3)</param>
/// <remarks>
/// Узлы являются корнями полинома Лежандра P3(x) = (5x^3 - 3x)/2.
/// Корни: -√(3/5), 0, √(3/5); веса: 5/9, 8/9, 5/9
/// </remarks>
void get_gauss_3(double t[], double c[]) {
    // Корни: 0, ±√(3/5)
    t[0] = -0.7745966692414834;  // -√(3/5)
    t[1] = 0.0;                  // 0
    t[2] = 0.7745966692414834;   // √(3/5)

    // Веса
    c[0] = 0.5555555555555556;   // 5/9
    c[1] = 0.8888888888888889;   // 8/9
    c[2] = 0.5555555555555556;   // 5/9
}

