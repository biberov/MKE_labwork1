// Квадратура Гаусса (общая функция)

/// <summary>
/// Общая функция для вычисления интеграла методом Гаусса-Лежандра
/// </summary>
/// <param name="f">Подынтегральная функция</param>
/// <param name="a">Нижний предел интегрирования</param>
/// <param name="b">Верхний предел интегрирования</param>
/// <param name="n">Количество узлов (допустимые значения: 3, 5, 7)</param>
/// <returns>Приближенное значение определенного интеграла</returns>
/// <remarks>
/// Квадратура Гаусса имеет порядок точности 2n-1.
/// Выполняется преобразование отрезка [a, b] к стандартному отрезку [-1, 1].
/// </remarks>
/// <exception cref="std::cerr">Выводит сообщение об ошибке при недопустимом n</exception>
double gauss_quadrature(double (*f)(double), double a, double b, int n) {
    if (n != 3 && n != 5 && n != 7) {
        std::cerr << "Ошибка: n должно быть 3, 5 или 7\n";
        return 0.0;
    }

    // Получаем узлы и веса для соответствующего n
    const int max_n = 7;
    double t[max_n], c[max_n];

    if (n == 3) get_gauss_3(t, c);
    else if (n == 5) get_gauss_5(t, c);
    else if (n == 7) get_gauss_7(t, c);

    // Преобразование [a, b] -> [-1, 1]
    double mid = (b + a) / 2.0;          // (b+a)/2
    double half_len = (b - a) / 2.0;     // (b-a)/2

    // Вычисляем сумму: ∑ c_k * f( (b+a)/2 + (b-a)/2 * t_k )
    double sum = 0.0;
    for (int i = 0; i < n; i++) {
        double x = mid + half_len * t[i];
        sum += c[i] * f(x);
    }

    // Умножаем на (b-a)/2
    return half_len * sum;
}

/// <summary>
/// Квадратура Гаусса с 3 узлами (упрощенный вызов)
/// </summary>
/// <param name="f">Подынтегральная функция</param>
/// <param name="a">Нижний предел интегрирования</param>
/// <param name="b">Верхний предел интегрирования</param>
/// <returns>Приближенное значение интеграла по формуле Гаусса с 3 узлами</returns>
/// <remarks>Метод имеет 5-й порядок точности</remarks>
double gauss_3(double (*f)(double), double a, double b) {
    return gauss_quadrature(f, a, b, 3);
}

/// <summary>
/// Квадратура Гаусса с 5 узлами (упрощенный вызов)
/// </summary>
/// <param name="f">Подынтегральная функция</param>
/// <param name="a">Нижний предел интегрирования</param>
