/// <returns>Приближенное значение интеграла по формуле Гаусса с 5 узлами</returns>
/// <remarks>Метод имеет 9-й порядок точности</remarks>
double gauss_5(double (*f)(double), double a, double b) {
    return gauss_quadrature(f, a, b, 5);
}

/// <summary>
/// Квадратура Гаусса с 7 узлами (упрощенный вызов)
/// </summary>
/// <param name="f">Подынтегральная функция</param>
/// <param name="a">Нижний предел интегрирования</param>
/// <param name="b">Верхний предел интегрирования</param>
